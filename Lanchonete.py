class Lanchonete:

    def get_id(self):
        return self.id

    def set_id(self, id):
        self.id = id

    def get_descricao(self):
        return self.descricao

    def set_descricao(self, descricao):
        self.descricao = descricao

    def get_qtd_clientes(self):
        return self.qtd_clientes

    def set_qtd_clientes(self, qtd_clientes):
        self.qtd_clientes = qtd_clientes

    def get_num_empregados(self):
        return self.num_empregados

    def set_num_empregados(self, num_empregados):
        self.num_empregados = num_empregados

    def get_total_receita(self):
        return self.total_receita

    def set_total_receita(self, total_receita):
        self.total_receita = total_receita

    def get_total_despesa(self):
        return self.total_despesa

    def set_total_despesa(self, total_despesa):
        self.total_despesa = total_despesa
