class Fornecedor:

    def get_id(self):
        return self.id

    def set_id(self, id):
        self.id = id

    def get_nome(self):
        return self.nome

    def set_nome(self, nome):
        self.nome = nome

    def get_telefone(self):
        return self.telefone

    def set_telefone(self, telefone):
        self.telefone = telefone

    def get_nome_produto_venda(self):
        return self.nome_produto_venda

    def set_nome_produto_venda(self, nome_produto_venda):
        self.nome_produto_venda = nome_produto_venda

    def get_segmento_produto(self):
        return self.segmento_produto

    def set_segmento_produto(self, segmento_produto):
        self.segmento_produto = segmento_produto

    def get_gasto_mensal(self):
        return self.gasto_mensal

    def set_gasto_mensal(self, gasto_mensal):
        self.gasto_mensal = gasto_mensal
